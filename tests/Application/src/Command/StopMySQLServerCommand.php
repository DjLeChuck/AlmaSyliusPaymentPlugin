namespace Tests\Alma\SyliusPaymentPlugin\App\Command;

use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Style\SymfonyStyle;
use Symfony\Component\Process\Process;

class StopMySQLServerCommand extends Command
{
    protected static $defaultName = 'alma:mysql:stop';

    protected function execute(InputInterface $input, OutputInterface $output): ?int
    {
        $io = new SymfonyStyle($input, $output);
        $io->title("Stopping test MySQL server");

        $process = new Process(explode(' ', "docker rm --force alma_mysql_test_server"));
        $process->run();

        if (!$process->isSuccessful()) {
            $io->error($process->getErrorOutput());
        }

        return $process->getExitCode();
    }
}
